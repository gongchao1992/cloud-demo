package com.wonders.base.domain;

import com.wonders.core.domain.BaseEntity;

import javax.persistence.*;

@Entity
@Table(name = "T_SYS_SUBSYSTEM")
public class SubSystemDO extends BaseEntity {
    private static final long serialVersionUID = 3528956833950511677L;

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private Long id;

    @Column(name = "name",nullable = false)
    private String name;

    @Column(name = "path",nullable = false)
    private String path;

    @Column(name = "order_num")
    private Integer orderNum;

    @Column(name = "remark")
    private String remark;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Integer getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(Integer orderNum) {
        this.orderNum = orderNum;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
